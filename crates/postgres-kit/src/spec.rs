//! The declarative schema DSL. A [`PgTableSpec`] is the single source of truth;
//! everything else the kit produces is derived from it.
//!
//! The DSL is builder-style and total: a column, table, enum, view, sequence,
//! role, foreign key, unique/check constraint, index, generated/identity column,
//! composite primary key, RLS toggle, and row-level policy can all be expressed
//! here. From this set you derive `CREATE TABLE` DDL ([`crate::to_create_table_sql`])
//! and the diffable [`crate::differ::SchemaSnapshot`] ([`crate::differ::lower`]).

/// A Postgres column type. The closed set keeps DDL generation total and the
/// future differ/codegen exhaustive. `Enum` names a user-defined enum type;
/// `Array` wraps any inner type.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum PgType {
    Uuid,
    Text,
    Varchar(Option<u32>),
    Bool,
    Int2,
    Int4,
    Int8,
    Float4,
    Float8,
    Numeric(Option<(u32, u32)>),
    Timestamptz,
    Timestamp,
    Date,
    Jsonb,
    Json,
    Bytea,
    /// Full-text search vector (`tsvector`).
    Tsvector,
    /// A pgvector embedding column. `Vector(None)` renders `vector`; `Vector(Some(n))`
    /// renders `vector(n)` (fixed-dimension).
    Vector(Option<u32>),
    /// A named Postgres enum type (e.g. `CREATE TYPE managed_website_status AS ENUM (...)`).
    Enum(String),
    /// An array of the inner type, rendered as `<inner>[]`.
    Array(Box<PgType>),
}

impl PgType {
    /// Render the canonical Postgres type text.
    pub fn to_sql_type(&self) -> String {
        match self {
            PgType::Uuid => "uuid".into(),
            PgType::Text => "text".into(),
            PgType::Varchar(None) => "varchar".into(),
            PgType::Varchar(Some(n)) => format!("varchar({n})"),
            PgType::Bool => "boolean".into(),
            PgType::Int2 => "smallint".into(),
            PgType::Int4 => "integer".into(),
            PgType::Int8 => "bigint".into(),
            PgType::Float4 => "real".into(),
            PgType::Float8 => "double precision".into(),
            PgType::Numeric(None) => "numeric".into(),
            PgType::Numeric(Some((p, s))) => format!("numeric({p},{s})"),
            PgType::Timestamptz => "timestamptz".into(),
            PgType::Timestamp => "timestamp".into(),
            PgType::Date => "date".into(),
            PgType::Jsonb => "jsonb".into(),
            PgType::Json => "json".into(),
            PgType::Bytea => "bytea".into(),
            PgType::Tsvector => "tsvector".into(),
            PgType::Vector(None) => "vector".into(),
            PgType::Vector(Some(n)) => format!("vector({n})"),
            PgType::Enum(name) => name.clone(),
            PgType::Array(inner) => format!("{}[]", inner.to_sql_type()),
        }
    }
}

/// A referential action for foreign-key `ON UPDATE` / `ON DELETE`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ReferentialAction {
    NoAction,
    Restrict,
    Cascade,
    SetNull,
    SetDefault,
}

impl ReferentialAction {
    /// Render the SQL keyword form (e.g. `SET NULL`).
    pub fn to_sql(self) -> &'static str {
        match self {
            ReferentialAction::NoAction => "NO ACTION",
            ReferentialAction::Restrict => "RESTRICT",
            ReferentialAction::Cascade => "CASCADE",
            ReferentialAction::SetNull => "SET NULL",
            ReferentialAction::SetDefault => "SET DEFAULT",
        }
    }
}

/// Whether an identity column is `GENERATED ALWAYS` or `GENERATED BY DEFAULT`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum IdentityKind {
    Always,
    #[default]
    ByDefault,
}

impl IdentityKind {
    pub fn to_sql(self) -> &'static str {
        match self {
            IdentityKind::Always => "ALWAYS",
            IdentityKind::ByDefault => "BY DEFAULT",
        }
    }
}

/// Sequence shaping options, shared by standalone sequences and identity columns.
/// All fields are optional verbatim numeric strings (trusted, developer-authored).
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct SequenceOptions {
    pub increment: Option<String>,
    pub min_value: Option<String>,
    pub max_value: Option<String>,
    pub start_with: Option<String>,
    pub cache: Option<String>,
    pub cycle: bool,
}

impl SequenceOptions {
    pub fn new() -> Self {
        Self::default()
    }
    pub fn increment(mut self, v: impl Into<String>) -> Self {
        self.increment = Some(v.into());
        self
    }
    pub fn min_value(mut self, v: impl Into<String>) -> Self {
        self.min_value = Some(v.into());
        self
    }
    pub fn max_value(mut self, v: impl Into<String>) -> Self {
        self.max_value = Some(v.into());
        self
    }
    pub fn start_with(mut self, v: impl Into<String>) -> Self {
        self.start_with = Some(v.into());
        self
    }
    pub fn cache(mut self, v: impl Into<String>) -> Self {
        self.cache = Some(v.into());
        self
    }
    pub fn cycle(mut self, on: bool) -> Self {
        self.cycle = on;
        self
    }
}

/// An identity column declaration (`GENERATED {ALWAYS|BY DEFAULT} AS IDENTITY`).
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct IdentitySpec {
    pub kind: IdentityKind,
    pub sequence: SequenceOptions,
}

impl IdentitySpec {
    pub fn always() -> Self {
        Self {
            kind: IdentityKind::Always,
            sequence: SequenceOptions::default(),
        }
    }
    pub fn by_default() -> Self {
        Self {
            kind: IdentityKind::ByDefault,
            sequence: SequenceOptions::default(),
        }
    }
    pub fn with_sequence(mut self, seq: SequenceOptions) -> Self {
        self.sequence = seq;
        self
    }
}

/// A `GENERATED ALWAYS AS (<expr>) STORED` column. The expression is verbatim,
/// trusted, developer-authored input.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct GeneratedColumn {
    pub expression: String,
}

/// Column-level uniqueness (`UNIQUE` / `UNIQUE NULLS NOT DISTINCT`).
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct ColumnUnique {
    /// Optional explicit constraint name.
    pub name: Option<String>,
    pub nulls_not_distinct: bool,
}

/// A single column. Construct with [`ColumnSpec::new`] and chain the builder
/// helpers (`.nullable()`, `.default_expr(...)`, `.generated_stored(...)`, …).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ColumnSpec {
    pub name: String,
    pub ty: PgType,
    pub nullable: bool,
    /// A trusted, developer-authored default expression (emitted verbatim).
    pub default: Option<String>,
    /// A `STORED` generated column expression, if any.
    pub generated: Option<GeneratedColumn>,
    /// An identity declaration, if any.
    pub identity: Option<IdentitySpec>,
    /// Column-level uniqueness, if any.
    pub unique: Option<ColumnUnique>,
}

impl ColumnSpec {
    pub fn new(name: impl Into<String>, ty: PgType) -> Self {
        Self {
            name: name.into(),
            ty,
            nullable: false,
            default: None,
            generated: None,
            identity: None,
            unique: None,
        }
    }

    pub fn nullable(mut self) -> Self {
        self.nullable = true;
        self
    }

    pub fn default_expr(mut self, expr: impl Into<String>) -> Self {
        self.default = Some(expr.into());
        self
    }

    /// Mark this column as a `STORED` generated column.
    pub fn generated_stored(mut self, expr: impl Into<String>) -> Self {
        self.generated = Some(GeneratedColumn {
            expression: expr.into(),
        });
        self
    }

    /// Attach an identity declaration.
    pub fn identity(mut self, identity: IdentitySpec) -> Self {
        self.identity = Some(identity);
        self
    }

    pub fn identity_always(self) -> Self {
        self.identity(IdentitySpec::always())
    }

    pub fn identity_by_default(self) -> Self {
        self.identity(IdentitySpec::by_default())
    }

    /// Mark the column `UNIQUE`.
    pub fn unique(mut self) -> Self {
        self.unique = Some(ColumnUnique::default());
        self
    }

    /// Mark the column `UNIQUE` with an explicit constraint name.
    pub fn unique_named(mut self, name: impl Into<String>) -> Self {
        self.unique = Some(ColumnUnique {
            name: Some(name.into()),
            nulls_not_distinct: false,
        });
        self
    }
}

/// A foreign-key constraint.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ForeignKeySpec {
    pub name: String,
    pub columns_from: Vec<String>,
    /// The referenced table, optionally schema-qualified (`schema.table`).
    pub table_to: String,
    pub columns_to: Vec<String>,
    pub on_update: Option<ReferentialAction>,
    pub on_delete: Option<ReferentialAction>,
}

impl ForeignKeySpec {
    pub fn new(
        name: impl Into<String>,
        columns_from: impl IntoIterator<Item = impl Into<String>>,
        table_to: impl Into<String>,
        columns_to: impl IntoIterator<Item = impl Into<String>>,
    ) -> Self {
        Self {
            name: name.into(),
            columns_from: columns_from.into_iter().map(Into::into).collect(),
            table_to: table_to.into(),
            columns_to: columns_to.into_iter().map(Into::into).collect(),
            on_update: None,
            on_delete: None,
        }
    }

    pub fn on_update(mut self, action: ReferentialAction) -> Self {
        self.on_update = Some(action);
        self
    }

    pub fn on_delete(mut self, action: ReferentialAction) -> Self {
        self.on_delete = Some(action);
        self
    }
}

