#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn renders_sql_types() {
        assert_eq!(PgType::Uuid.to_sql_type(), "uuid");
        assert_eq!(PgType::Varchar(Some(255)).to_sql_type(), "varchar(255)");
        assert_eq!(
            PgType::Numeric(Some((10, 2))).to_sql_type(),
            "numeric(10,2)"
        );
        assert_eq!(
            PgType::Array(Box::new(PgType::Text)).to_sql_type(),
            "text[]"
        );
        assert_eq!(PgType::Enum("status".into()).to_sql_type(), "status");
        assert_eq!(PgType::Timestamptz.to_sql_type(), "timestamptz");
        assert_eq!(PgType::Float8.to_sql_type(), "double precision");
        assert_eq!(PgType::Tsvector.to_sql_type(), "tsvector");
        assert_eq!(PgType::Vector(None).to_sql_type(), "vector");
        assert_eq!(PgType::Vector(Some(1536)).to_sql_type(), "vector(1536)");
    }

    #[test]
    fn builder_sets_flags() {
        let c = ColumnSpec::new("x", PgType::Int4)
            .nullable()
            .default_expr("0");
        assert!(c.nullable);
        assert_eq!(c.default.as_deref(), Some("0"));

        let pk = PgTableSpec::new("t", vec![c]).primary_key(["x"]);
        assert_eq!(pk.primary_key, vec!["x".to_string()]);
    }

    #[test]
    fn column_builders_for_generated_identity_unique() {
        let g = ColumnSpec::new("full", PgType::Text).generated_stored("a || b");
        assert_eq!(g.generated.as_ref().unwrap().expression, "a || b");

        let id = ColumnSpec::new("n", PgType::Int8).identity_always();
        assert_eq!(id.identity.as_ref().unwrap().kind, IdentityKind::Always);

        let u = ColumnSpec::new("email", PgType::Text).unique_named("u_email");
        assert_eq!(u.unique.as_ref().unwrap().name.as_deref(), Some("u_email"));
    }

    #[test]
    fn table_builders_collect_constraints() {
        let t = PgTableSpec::new("users", vec![ColumnSpec::new("id", PgType::Uuid)])
            .in_schema("app")
            .primary_key(["id"])
            .foreign_key(
                ForeignKeySpec::new("fk_org", ["org_id"], "public.orgs", ["id"])
                    .on_delete(ReferentialAction::Cascade),
            )
            .unique_constraint(UniqueConstraintSpec::new("u_email", ["email"]))
            .check(CheckConstraintSpec::new("c_pos", "n > 0"))
            .index(IndexSpec::new("idx_email", [IndexColumn::column("email")]).unique())
            .policy(PolicySpec::new("p").for_command(PolicyFor::Select))
            .enable_rls();

        assert_eq!(t.qualified_name(), "app.users");
        assert_eq!(t.foreign_keys.len(), 1);
        assert_eq!(
            t.foreign_keys[0].on_delete,
            Some(ReferentialAction::Cascade)
        );
        assert_eq!(t.unique_constraints.len(), 1);
        assert_eq!(t.check_constraints.len(), 1);
        assert_eq!(t.indexes.len(), 1);
        assert_eq!(t.policies.len(), 1);
        assert!(t.rls_enabled);
    }

    #[test]
    fn top_level_specs_default_to_public() {
        assert_eq!(
            EnumTypeSpec::new("status", ["a", "b"]).qualified_name(),
            "public.status"
        );
        assert_eq!(ViewSpec::new("v", "SELECT 1").qualified_name(), "public.v");
        assert_eq!(SequenceSpec::new("s").qualified_name(), "public.s");
        let r = RoleSpec::new("app").create_db(true);
        assert!(r.create_db && r.inherit);
    }
}
