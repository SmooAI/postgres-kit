/// One column (or expression) participating in an index.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct IndexColumn {
    /// Column name, or a raw expression when `is_expression` is true.
    pub expression: String,
    pub is_expression: bool,
    pub asc: bool,
    /// `NULLS FIRST` / `NULLS LAST`, if specified.
    pub nulls: Option<String>,
    /// Operator class, if specified.
    pub opclass: Option<String>,
}

impl IndexColumn {
    /// A plain ascending column reference.
    pub fn column(name: impl Into<String>) -> Self {
        Self {
            expression: name.into(),
            is_expression: false,
            asc: true,
            nulls: None,
            opclass: None,
        }
    }

    /// A raw expression index member (e.g. `lower(email)`).
    pub fn expr(expr: impl Into<String>) -> Self {
        Self {
            expression: expr.into(),
            is_expression: true,
            asc: true,
            nulls: None,
            opclass: None,
        }
    }

    pub fn desc(mut self) -> Self {
        self.asc = false;
        self
    }

    pub fn nulls(mut self, nulls: impl Into<String>) -> Self {
        self.nulls = Some(nulls.into());
        self
    }

    pub fn opclass(mut self, opclass: impl Into<String>) -> Self {
        self.opclass = Some(opclass.into());
        self
    }
}

/// A (possibly partial / unique / expression) index.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct IndexSpec {
    pub name: String,
    pub columns: Vec<IndexColumn>,
    pub unique: bool,
    /// Access method (`btree`, `gin`, …). Defaults to `btree`.
    pub method: String,
    /// A partial-index `WHERE` predicate (trusted expression).
    pub where_clause: Option<String>,
}

impl IndexSpec {
    pub fn new(name: impl Into<String>, columns: impl IntoIterator<Item = IndexColumn>) -> Self {
        Self {
            name: name.into(),
            columns: columns.into_iter().collect(),
            unique: false,
            method: "btree".into(),
            where_clause: None,
        }
    }

    pub fn unique(mut self) -> Self {
        self.unique = true;
        self
    }

    pub fn method(mut self, method: impl Into<String>) -> Self {
        self.method = method.into();
        self
    }

    pub fn where_clause(mut self, predicate: impl Into<String>) -> Self {
        self.where_clause = Some(predicate.into());
        self
    }
}

/// The `AS` permissiveness of a row-level-security policy.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PolicyAs {
    Permissive,
    Restrictive,
}

impl PolicyAs {
    pub fn to_sql(self) -> &'static str {
        match self {
            PolicyAs::Permissive => "PERMISSIVE",
            PolicyAs::Restrictive => "RESTRICTIVE",
        }
    }
}

/// The command a policy applies to.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PolicyFor {
    All,
    Select,
    Insert,
    Update,
    Delete,
}

impl PolicyFor {
    pub fn to_sql(self) -> &'static str {
        match self {
            PolicyFor::All => "ALL",
            PolicyFor::Select => "SELECT",
            PolicyFor::Insert => "INSERT",
            PolicyFor::Update => "UPDATE",
            PolicyFor::Delete => "DELETE",
        }
    }
}

/// A row-level-security policy on a table.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct PolicySpec {
    pub name: String,
    pub as_: Option<PolicyAs>,
    pub for_: Option<PolicyFor>,
    /// Roles the policy applies `TO`.
    pub to: Vec<String>,
    /// The `USING` predicate (trusted expression).
    pub using: Option<String>,
    /// The `WITH CHECK` predicate (trusted expression).
    pub with_check: Option<String>,
}

impl PolicySpec {
    pub fn new(name: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            as_: None,
            for_: None,
            to: Vec::new(),
            using: None,
            with_check: None,
        }
    }

    pub fn as_permissiveness(mut self, as_: PolicyAs) -> Self {
        self.as_ = Some(as_);
        self
    }

    pub fn for_command(mut self, for_: PolicyFor) -> Self {
        self.for_ = Some(for_);
        self
    }

    pub fn to_roles(mut self, roles: impl IntoIterator<Item = impl Into<String>>) -> Self {
        self.to = roles.into_iter().map(Into::into).collect();
        self
    }

    pub fn using(mut self, predicate: impl Into<String>) -> Self {
        self.using = Some(predicate.into());
        self
    }

    pub fn with_check(mut self, predicate: impl Into<String>) -> Self {
        self.with_check = Some(predicate.into());
        self
    }
}

/// A table declaration — the unit of the schema source of truth.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct PgTableSpec {
    /// The containing schema. Defaults to `public`.
    pub schema: String,
    pub name: String,
    pub columns: Vec<ColumnSpec>,
    pub primary_key: Vec<String>,
    pub foreign_keys: Vec<ForeignKeySpec>,
    pub unique_constraints: Vec<UniqueConstraintSpec>,
    pub check_constraints: Vec<CheckConstraintSpec>,
    pub indexes: Vec<IndexSpec>,
    pub policies: Vec<PolicySpec>,
    pub rls_enabled: bool,
}

impl PgTableSpec {
    pub fn new(name: impl Into<String>, columns: Vec<ColumnSpec>) -> Self {
        Self {
            schema: "public".into(),
            name: name.into(),
            columns,
            primary_key: Vec::new(),
            foreign_keys: Vec::new(),
            unique_constraints: Vec::new(),
            check_constraints: Vec::new(),
            indexes: Vec::new(),
            policies: Vec::new(),
            rls_enabled: false,
        }
    }

    /// Place the table in a non-`public` schema.
    pub fn in_schema(mut self, schema: impl Into<String>) -> Self {
        self.schema = schema.into();
        self
    }

    pub fn primary_key(mut self, cols: impl IntoIterator<Item = impl Into<String>>) -> Self {
        self.primary_key = cols.into_iter().map(Into::into).collect();
        self
    }

    pub fn foreign_key(mut self, fk: ForeignKeySpec) -> Self {
        self.foreign_keys.push(fk);
        self
    }

    pub fn unique_constraint(mut self, uc: UniqueConstraintSpec) -> Self {
        self.unique_constraints.push(uc);
        self
    }

    pub fn check(mut self, cc: CheckConstraintSpec) -> Self {
        self.check_constraints.push(cc);
        self
    }

    pub fn index(mut self, idx: IndexSpec) -> Self {
        self.indexes.push(idx);
        self
    }

    pub fn policy(mut self, policy: PolicySpec) -> Self {
        self.policies.push(policy);
        self
    }

    pub fn enable_rls(mut self) -> Self {
        self.rls_enabled = true;
        self
    }

    /// The `schema.table` key used as the snapshot identity.
    pub fn qualified_name(&self) -> String {
        format!("{}.{}", self.schema, self.name)
    }
}

